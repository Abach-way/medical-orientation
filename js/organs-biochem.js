function openOrganDetail(organKey) {
    const organ = organData[organKey];
    if (!organ) return;

    const modal = document.getElementById('organ-detail-modal');
    document.getElementById('organ-detail-name').textContent = organ.name;
    document.getElementById('organ-detail-latin').textContent = organ.latin;

    // Insert 3D model via Three.js
    const container3d = document.getElementById('organ-detail-3d');
    container3d.innerHTML = '';
    if (typeof Organs3DEngine !== 'undefined') {
        Organs3DEngine.initModal(organKey);
    }

    // Default tab = anatomy
    switchOrganTab('anatomy', null, organ);

    // Key facts
    const factsGrid = document.getElementById('organ-facts-grid');
    factsGrid.innerHTML = organ.facts.map(f =>
        `<div class="organ-fact-item">
            <span class="organ-fact-value">${f.value}</span>
            <span class="organ-fact-label">${f.label}</span>
        </div>`
    ).join('');

    modal.classList.add('active');
    document.body.style.overflow = 'hidden';

    // Store current organ for tab switching
    modal.dataset.currentOrgan = organKey;
}

function closeOrganDetail() {
    const modal = document.getElementById('organ-detail-modal');
    modal.classList.remove('active');
    document.body.style.overflow = '';
}

function switchOrganTab(tab, btnEl, organObj) {
    const modal = document.getElementById('organ-detail-modal');
    const organ = organObj || organData[modal.dataset.currentOrgan];
    if (!organ) return;

    // Update active tab
    if (btnEl) {
        document.querySelectorAll('.organ-tab').forEach(t => t.classList.remove('active'));
        btnEl.classList.add('active');
    } else {
        // Default to first tab
        const tabs = document.querySelectorAll('.organ-tab');
        tabs.forEach(t => t.classList.remove('active'));
        if (tabs[0]) tabs[0].classList.add('active');
    }

    const content = document.getElementById('organ-tab-content');
    content.innerHTML = organ[tab] || '<p>–ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –∑–∞–≥—Ä—É–∂–∞–µ—Ç—Å—è...</p>';
}


